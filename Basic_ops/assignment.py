""" This code will introduce you assignment operators in python 
    we have only one assignment operator =, but we can use this as shortcut operator too """

# '=' operator will help you to assign value at rightside to left side variable 
A=5 #says variable called A is assigned value of 5 

A+=1 #This will assign value of A+1 to A so if you print A now value will be 6 
print("Value of A+=1 is:",A)

#you can use any arithmetic operator as shortcut operator to assignment operator 

A*=3 #this will give A value of A*3 since A was 6 now A will become 18
print("Value of A*=3 is:",A)