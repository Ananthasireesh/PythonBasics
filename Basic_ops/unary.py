""" This code will introduce you unary operators in python 
    Unary operators are basically used to assign a sign to a variable """

""" There are only two unary operators Unary Plus(+) and Unary(-)"""
a=5
print("value of +a is:",+a)
print("value of -a is:",-a)

b=-3
print("value of +b is:",+b)
print("value of -b is:",-b)